# 풀이방법: 모든 가짓수를 세는 것 + 점화식 => DP

# n=1 : 1, 
# n=2 : 00 / 11, 
# n=3 : 100 / 001, 111
# n=4 : 0000, 1100 / 1001, 0011 , 1111
# n=5 : 00001, 00100, 10000 / 00111, 10011, 11001, 11100 / 11111
# n=6 : 000000, 001100, 100100, 110000, 111100 / 000011,001001,100001,001111,100111,110011,111001,111111

n = int(input())

dp = [0] * 1000001

dp[1] = 1
dp[2] = 2
for k in range(3, n+1):
    dp[k] = (dp[k-1] + dp[k-2])%15746

print(dp[n])
    